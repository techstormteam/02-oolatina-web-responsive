<p>Les technologies telles que les cookies, les pixels de suivi (« pixels ») et le stockage local sont utilisées pour afficher, protéger et comprendre</p>
<p>les produits, les services et les publicités, sur Facebook comme au-dehors. Cette page est conçue pour vous aider à mieux comprendre ces technologies</p>
<p>et la manière dont elles sont utilisées. Votre navigateur ou votre appareil mobile peut vous permettre de bloquer ces technologies, mais cela vous </p>
<p>empêchera également d’utiliser certaines fonctionnalités de Facebook. Pour en savoir plus sur la disponibilité de ces outils, leur fonction et leur</p>
<p>fonctionnement, consultez l’aide de votre navigateur ou de votre appareil. Généralement, ces types d’outils dans votre navigateur ou votre appareil</p>
<p>affectent uniquement le navigateur ou l’appareil en question. Ainsi, si vous utilisez des navigateurs ou des appareils variés, vous pouvez faire des</p>
<p>choix différents sur chacun.</p>
